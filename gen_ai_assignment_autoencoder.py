DATA_PATH = image_folder

def load_images(path, limit=5000):
    data = []
    files = os.listdir(path)[:limit]

    for file in files:
        img_path = os.path.join(path, file)
        img = cv2.imread(img_path)

        if img is None:
            continue

        img = cv2.resize(img, (128, 128))
        img = img / 255.0
        data.append(img)

    return np.array(data)
images = load_images(DATA_PATH, limit=5000)

def add_noise(imgs):
    noise = np.random.normal(0, 0.2, imgs.shape)
    noisy_imgs = imgs + noise
    noisy_imgs = np.clip(noisy_imgs, 0., 1.)
    return noisy_imgs

noisy_images = add_noise(images)
print(images.shape)
print(noisy_images.shape)

from tensorflow.keras.layers import Input, Conv2D, MaxPooling2D, UpSampling2D
from tensorflow.keras.models import Model

input_img = Input(shape=(128, 128, 3))

# Encoder
x = Conv2D(64, (3,3), activation='relu', padding='same')(input_img)
x = MaxPooling2D((2,2), padding='same')(x)

x = Conv2D(32, (3,3), activation='relu', padding='same')(x)
encoded = MaxPooling2D((2,2), padding='same')(x)

# Decoder
x = Conv2D(32, (3,3), activation='relu', padding='same')(encoded)
x = UpSampling2D((2,2))(x)

x = Conv2D(64, (3,3), activation='relu', padding='same')(x)
x = UpSampling2D((2,2))(x)

decoded = Conv2D(3, (3,3), activation='sigmoid', padding='same')(x)

autoencoder = Model(input_img, decoded)
autoencoder.compile(optimizer='adam', loss='mse')

autoencoder.summary()

autoencoder.fit(
    noisy_images,
    images,
    epochs=10,
    batch_size=32,
    validation_split=0.1
)

decoded_imgs = autoencoder.predict(noisy_images)
mse = np.mean((images - decoded_imgs) ** 2)
print("Reconstruction Error (MSE):", mse)
import matplotlib.pyplot as plt

n = 5
